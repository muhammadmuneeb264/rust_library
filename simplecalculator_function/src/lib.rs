pub fn calculator_function(c:f64,d:f64) 
{

    let sum : f64 =  c + d ; 
    let sub : f64 =  c - d ;
    let multi : f64 = c * d ;
    let divi : f64 = c / d ;
    let modul : f64 = c % d ;
println!("Addition = {}",sum);
println!("Subtraction =  {}",sub);
println!(" multiplication = {}",multi);
println!(" division = {}",divi);
println!(" modulus = {}",modul);
  
}
